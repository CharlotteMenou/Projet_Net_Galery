 <h1>Aglae Bory</h1>

    <h3>Biographie</h3>

    <p>Aglae Bory est une photographe originaire d'Alsace qui vit et travail en region parisienne.</p>

    <p>Apres avoir etudie l'histoire de l'Art a l'universite d'Aix-en-Provence
    et la photographie a l'ecole Nationale de Photographie d'Arles, Aglae Bory
    travail a Paris ou en parallele de son travail artistique personnel, elle collabore
    regulierement avec la presse et des agences de communication.</p>

    <h3>Sa demarche artistique</h3>

    <p>"Mon ecriture photographique se situe entre document et fiction. Je procede d'une facon qu'on peut comparer a celle d'un ecrivain, j'ecris grace a la lumiere et a l'appareil photographique des histoires qui contiennent des idees, des sentiments, qu'il m'est necessaire de mettre en images. Je tente de mettre en lumiere ce qui est de l'ordre de l'invisible, comme les emotions, le hors champs, le monde interieur. La photographie donne a voir, montre, fixe. Je cherche avoir ce qui ne se voit pas, a montrer ce qui est cache, a fixer ce qui est volatil. Le fait que la photographie soit un medium du visible attise encore plus mon ambition de photographier le non visible. C'est dans cette sorte de paradoxe que je trouve le sens de l'acte photographique. C'est dans ce mince interstice entre le visible et le reard que j'eprouve la poesie du monde. Il s'opere ainsi une concordance entre mon ecriture photographique et une certaine ecriture litteraire, les images remplacant les mots."</p>

